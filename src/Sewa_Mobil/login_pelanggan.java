    private void loginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_loginActionPerformed
        // TODO add your handling code here:
        String id=id_pelanggan.getText();
        String password1=password.getText();
        
        try {
            Connection kon = DriverManager.getConnection("jdbc:mysql://localhost/sewamobil","root","");
            ResultSet as = kon.createStatement().executeQuery("select * from pelanggan where id_pelanggan='"+id+"' and password='"+password1+"'");
            while(as.next()){
                id=as.getString("id_pelanggan");
            }
            as.last();
            
            if(as.getRow()==1){
                register.setid(id);
                JOptionPane.showMessageDialog(rootPane, "Anda Berhasil Login");
                hal_customer ha = new hal_customer();
                ha.setVisible(true);
                this.dispose();
            } else{
                JOptionPane.showMessageDialog(rootPane, "Password Atau ID salah");
            }
        } 
        catch (SQLException ex) {
            Logger.getLogger(login_pelanggan.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_loginActionPerformed

